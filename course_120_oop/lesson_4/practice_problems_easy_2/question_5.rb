# There are a number of variables listed below. What are the different types and how do you know which is which?
  
  # Answers:
  # Local variable:
  excited_dog = "excited dog"
  # Local variables follow the snake case practice - all lower letters, seperating words by underscores

  # Instance variable
  @excited_dog = "excited dog"
  # Instance variables are also all lower letters and snake case - but prepended by a single '@'
  
  # Class variable
  @@excited_dog = "excited dog"
  # Class variables are all lower letters and snake case - but prepended by two '@'