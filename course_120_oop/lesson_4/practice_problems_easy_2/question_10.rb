# What are the benefits of using Object Oriented Programming in Ruby? Think of as many as you can.

=begin
Answer:

#1. It gives us another level of abstraction to work with.
    We can abstract the logic into something more related to the real world

#2. It gives us a "template" to work with so we don't have to manually setup each object.

#3. It provides us with a more natural hierarchilly structure of objects and functions

#4. It makes our code more DRY

#5. Encapsulation makes our program less prone to have a ripple effect of errors.

#6. It allows us to only expose methods to the parts of the program that need it - making name spacing
    issues a lot harder to encounter.

#7. It's easier to manage bigger code bases.
=end