# 10. Can hash values be arrays? Can you have an array of hashes? (give examples)

#  Can hash values be arrays - Yes:
h =  {skateboard_parts: ["trucks", "board", "wheels", "griptape", "bearings"],
      ages: [24, 16, 32, 83, 42]}
p h

# Can you have an array of hashes? - Yes:
a =  [{a: 1, b: 2, c: 3},
      {d: 4, e: 5}]

p a
