// What is exception handling and what problem does it solve?

/*
Exception handling is code written for the specific purpose of handling exceptions that might occur
in a section of code in a predictable manner.
It solves the problem that might arise from not handling such an exception - whether that'd be producing
innacurate data or breaking the code.

Exception handling in JavaScript uses the keywords try / catch.
The try block will run the code and if an exception is thrown, the code in the catch block will take over.
*/