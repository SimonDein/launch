def rot_13(encrypted_text)
  encrypted_text.each_char.inject('') do |result, encrypted_char|
    result + decipher_char(encrypted_char)
  end
end

def decipher_char(encrypted_char)
  case encrypted_char
  when 'a'..'m', 'A'..'M' then (encrypted_char.ord + 13).chr
  when 'n'..'z', 'N'..'Z' then (encrypted_char.ord - 13).chr
  else encrypted_char
  end
end

